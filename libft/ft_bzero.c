#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*res;
	size_t			i;

	i = 0;
	res = (unsigned char*)s;
	while (i < n)
	{
		res[i] = 0;
		i++;
	}
}
